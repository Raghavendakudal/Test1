
public class Upcasting {

	public static void main(String[] args) {

		/*
		 * int i = 10; double d = i; long l = i; System.out.println("value of d is:" +
		 * d); System.out.println("value of l is :" + l);
		 * 
		 * long L = 2000; int n = (int) L; System.out.println("value of n is:" + n);
		 */

		/*
		 * String s = "10"; int a = Integer.parseInt(s);
		 * System.out.println("value of a is:" + a);
		 * 
		 * long b = Long.parseLong(s); System.out.println("value of b is :" + b);
		 * 
		 * double c = Double.parseDouble(s); System.out.println("value of c is :" + c);
		 */

		int z = 25;
		String ss = String.valueOf(z);
		System.out.println(ss);
	}

}
